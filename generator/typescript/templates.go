package typescript

// Templates 返回所有 TypeScript 模板
func Templates() []string {
	return []string{
		fileTemplate,
		importsTemplate,
		enumTemplate,
		structTemplate,
		serviceTemplate,
		constantTemplate,
		typedefTemplate,
	}
}

// 文件模板
const fileTemplate = `
{{- define "thrift" -}}
"use strict";

// Generated by thriftgo {{Version}}
// DO NOT EDIT UNLESS YOU ARE SURE THAT YOU KNOW WHAT YOU ARE DOING

{{- if .Package }}
// Package: {{.Package}}
{{- end }}

{{- if .Imports }}
{{ template "imports" . }}
{{- end }}

{{- range .Constants }}
{{ template "constant" . }}
{{- end }}

{{- range .Typedefs }}
{{ template "typedef" . }}
{{- end }}

{{- range .Enums }}
{{ template "enum" . }}
{{- end }}

{{- range .Structs }}
{{ template "struct" . }}
{{- end }}

{{- range .Unions }}
{{ template "union" . }}
{{- end }}

{{- range .Exceptions }}
{{ template "exception" . }}
{{- end }}

{{- range .Services }}
{{ template "service" . }}
{{- end }}
{{- end -}}
`

// 导入模板
const importsTemplate = `
{{- define "imports" -}}
{{- range .Imports }}
import { {{ range $index, $type := .Types }}{{ if $index }}, {{ end }}{{ $type }}{{ end }} } from './{{ .Module }}';
{{- end }}
{{- end -}}
`

// 枚举模板
const enumTemplate = `
{{- define "enum" -}}
export enum {{ GetEnumName .Name }} {
{{- range .Values }}
  {{ GetConstantName .Name }} = {{ .Value }},
{{- end }}
}
{{- end -}}
`

// 结构体模板
const structTemplate = `
{{- define "struct" -}}
export interface {{ GetInterfaceName .Name }} {
{{- $expandedFields := GetExpandedFields . }}
{{- $expandedFieldNames := GetExpandedFieldNames . }}
{{- range .Fields }}
{{- if not (index $expandedFieldNames .Name) }}
  {{ GetPropertyName .Name }}{{ if IsOptional . }}?{{ end }}: {{ GetFieldType . }};
{{- end }}
{{- end }}
{{- if $expandedFields }}
{{- range $expandedFields }}
  {{ GetPropertyName .Name }}{{ if IsOptional . }}?{{ end }}: {{ GetFieldType . }};
{{- end }}
{{- end }}
}
{{- end -}}
`

// 联合体模板
const unionTemplate = `
{{- define "union" -}}
export interface {{ GetInterfaceName .Name }} {
{{- range .Fields }}
  {{ GetPropertyName .Name }}{{ if IsOptional . }}?{{ end }}: {{ GetFieldType . }};
{{- end }}
}
{{- end -}}
`

// 异常模板
const exceptionTemplate = `
{{- define "exception" -}}
export interface {{ GetInterfaceName .Name }} {
{{- range .Fields }}
  {{ GetPropertyName .Name }}{{ if IsOptional . }}?{{ end }}: {{ GetFieldType . }};
{{- end }}
}
{{- end -}}
`

// 服务模板
const serviceTemplate = `
{{- define "service" -}}
export interface {{ GetInterfaceName .Name }} {
{{- range .Functions }}
  {{ GetPropertyName .Name }}{{ GetMethodSignature . }};
{{- end }}
}
{{- end -}}
`

// 常量模板
const constantTemplate = `
{{- define "constant" -}}
export const {{ GetConstantName .Name }}: {{ GetTypeScriptType .Type }} = {{ GetConstantValue . }};
{{- end -}}
`

// 类型定义模板
const typedefTemplate = `
{{- define "typedef" -}}
export type {{ GetInterfaceName .Alias }} = {{ GetTypeScriptType .Type }};
{{- end -}}
`
